def main():
    num1 = 10
    num2 = 5

    sum = num1 + num2
    print(f"合計: {sum}")

    diff = num1 - num2
    print(f"差: {diff}")

    mul = num1 * num2
    print(f"積: {mul}")

    div = num1 // num2
    print(f"商: {div}")

    mod = num1 % num2
    print(f"余り: {mod}")

if __name__ == "__main__":
    main()